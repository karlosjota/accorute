package su.msu.cs.lvk.accorute.http.constants;

/**
 * The enumeration class, which list common HTTP methods according to <a href="http://www.w3.org/Protocols/rfc2616/rfc2616.html">RFC 2616</a>.
 */
public class HTTPMethod {
    /**
     * The RFC 2616 GET method.
     */
    public static final String GET = "GET";
    /**
     * The RFC 2616 POST method.
     */
    public static final String POST = "POST";
    /**
     * The RFC 2616 HEAD method.
     */
    public static final String HEAD = "HEAD";
    /**
     * The RFC 2616 PUT method.
     */
    public static final String PUT = "PUT";
    /**
     * The RFC 2616 DELETE method.
     */
    public static final String DELETE = "DELETE";
    /**
     * The RFC 2616 OPTIONS method.
     */
    public static final String OPTIONS = "OPTIONS";
    /**
     * The RFC 2616 TRACE method.
     */
    public static final String TRACE = "TRACE";

}
